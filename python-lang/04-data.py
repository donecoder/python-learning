# 数据类型

# 1、整数
print(123)
print(0x34a2)

# 2、浮点数
print(6.36)
print(1.23e9)

# 3、字符串
print('hello,world')
print("I'm huang")

# 4、布尔型
print(True)
print(False)
print(3 > 4)

# 5、空值
print(None)

# 常量 :常量就是不能变的量，比如常用的数学常数π就是一个常量。在Python中，通常用全部大写的变量名表示常量
PI = 3.14159265359
print(PI)

# 变量 : 变量在程序中就是用一个变量名表示了，变量名必须是大小写英文、数字和_的组合，且不能用数字开头
a = 123 # a是整数
print(a)
a = 'ABC' # a变为字符串
print(a)

